/*
비교 연산자 >, >=, <, <=, ==, !=, ===, !==
- 비교 연산자는 두 값을 비교하여 true/false 값을 반환합니다.
- == (동등 연산자)는 값만 비교하고, === (일치 연산자)는 값과 타입을 모두 비교합니다.
*/

var a = 5;      // 숫자형 5
var b = 10;     // 숫자형 10
var c = '5';    // 문자열 '5'
var d = 5;      // 숫자형 5

// > (크다): 왼쪽 값이 오른쪽 값보다 큰지 비교
console.log(a > b); // 5 > 10 = false (5는 10보다 크지 않음)

// < (작다): 왼쪽 값이 오른쪽 값보다 작은지 비교
console.log(a < b); // 5 < 10 = true (5는 10보다 작음)

// >= (크거나 같다): 왼쪽 값이 오른쪽 값보다 크거나 같은지 비교
console.log(a >= d); // 5 >= 5 = true (5는 5와 같음)

// <= (작거나 같다): 왼쪽 값이 오른쪽 값보다 작거나 같은지 비교
console.log(a <= d); // 5 <= 5 = true (5는 5와 같음)

// == (동등 연산자): 값만 비교 (타입 변환 발생)
console.log(a == c); // 5 == '5' => true (문자열 '5'가 숫자 5로 변환되어 비교)

// === (일치 연산자): 값과 타입을 모두 비교 (타입 변환 없음)
console.log(a === c); // 5 === '5' => false (숫자와 문자열은 타입이 다름)

// 문자열 비교: 사전순(lexicographical order)으로 비교
console.log('ch05' > 'ch01'); // true ('05'가 '01'보다 사전순으로 뒤에 위치)
console.log('ch12' > 'ch2');  // false ('12'가 '2'보다 사전순으로 앞에 위치)

// 사전순 정렬 예시
// ch01, ch05, ch12, ch2
// 문자열 비교는 각 문자를 순서대로 비교하며, 숫자도 문자로 취급하여 비교합니다.
